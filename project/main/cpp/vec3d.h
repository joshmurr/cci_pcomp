#ifndef VEC3D_H_
#define VEC3D_H_

#include <math.h>
#include <cmath>
#include <vector>

class Vec3d{
    private:

    public:
        Vec3d();
        Vec3d(double x, double y, double z);
        Vec3d operator+ (const Vec3d &v);
        Vec3d operator- (const Vec3d &v);
        Vec3d operator* (const Vec3d &v);
        Vec3d operator/ (const Vec3d &v);
        Vec3d abs();
        double dot(const Vec3d &v);
        double length();
        void normalise();

    private:

    public:
        double x, y, z;

};


#endif
